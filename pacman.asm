# $t0 - Numero de casilla actual
# $t1 - Valor en casilla actual
# $t2 - Direccion de plantilla de dibujo de casilla
# $t3 - Numero de pixel en casilla actual
# $t4 - Direccion base en memoria de la casilla actual en pantalla
# $t5 - Direccion del pixel actual
# $t6 - Color del pixel
dibujarTablero:
		sub $sp, $sp, 4								# Almacenar $ra
		sw $ra, ($sp)
		move $t0, $zero								# Inicializar en 1ra casilla del tablero

		dibujarCasillas:		
				beq $t0, 1024, finDibujarTablero			# Terminar si se dibujaron las 32x32 casillas
