						beq $t1, 0, dibujarPacManArribaAbierto
						beq $t1, 1, dibujarPacManDerechaAbierto
						beq $t1, 2, dibujarPacManAbajoAbierto
						beq $t1, 3, dibujarPacManIzquierdaAbierto
				
						dibujarPacManDerechaAbierto:
								la $a1, dibujoPacManDerechaAbierto									
								j continuarDibujarCasillas																																											
						dibujarPacManIzquierdaAbierto:
								la $a1, dibujoPacManIzquierdaAbierto									
								j continuarDibujarCasillas
						dibujarPacManArribaAbierto:
								la $a1, dibujoPacManArribaAbierto									
								j continuarDibujarCasillas
						dibujarPacManAbajoAbierto:
								la $a1, dibujoPacManAbajoAbierto									
								j continuarDibujarCasillas				
				
				dibujarPacManCerrado:
						beq $t1, 0, dibujarPacManArribaCerrado
						beq $t1, 1, dibujarPacManDerechaCerrado
						beq $t1, 2, dibujarPacManAbajoCerrado
						beq $t1, 3, dibujarPacManIzquierdaCerrado
				
						dibujarPacManDerechaCerrado:
								la $a1, dibujoPacManDerechaCerrado									
								j continuarDibujarCasillas																																											
						dibujarPacManIzquierdaCerrado:
								la $a1, dibujoPacManIzquierdaCerrado
								j continuarDibujarCasillas
						dibujarPacManArribaCerrado:
								la $a1, dibujoPacManArribaCerrado
								j continuarDibujarCasillas
						dibujarPacManAbajoCerrado:
								la $a1, dibujoPacManAbajoCerrado
								j continuarDibujarCasillas				
				
				
		dibujarFantasma1:					# Guardar direccion en memoria de plantilla de dibujo del fantasma 1
				lw $t0, direccionF1
				beq $t0, 0, dibujarFantasma1Arriba
				beq $t0, 1, dibujarFantasma1Derecha
				beq $t0, 2, dibujarFantasma1Abajo
				beq $t0, 3, dibujarFantasma1Izquierda
				
				dibujarFantasma1Derecha:
						la $a1, dibujoFantasma1Derecha									
						j continuarDibujarCasillas																																											
				dibujarFantasma1Izquierda:
						la $a1, dibujoFantasma1Izquierda									
						j continuarDibujarCasillas
				dibujarFantasma1Arriba:
						la $a1, dibujoFantasma1Arriba									
						j continuarDibujarCasillas
				dibujarFantasma1Abajo:
						la $a1, dibujoFantasma1Abajo									
						j continuarDibujarCasillas			
			
		dibujarFantasma2:					# Guardar direccion en memoria de plantilla de dibujo del fantasma 2
				lw $t0, direccionF2
				beq $t0, 0, dibujarFantasma2Arriba
				beq $t0, 1, dibujarFantasma2Derecha
				beq $t0, 2, dibujarFantasma2Abajo
				beq $t0, 3, dibujarFantasma2Izquierda
				
				dibujarFantasma2Derecha:
						la $a1, dibujoFantasma2Derecha									
						j continuarDibujarCasillas																																											
				dibujarFantasma2Izquierda:
						la $a1, dibujoFantasma2Izquierda									
						j continuarDibujarCasillas
				dibujarFantasma2Arriba:
						la $a1, dibujoFantasma2Arriba									
						j continuarDibujarCasillas
				dibujarFantasma2Abajo:
						la $a1, dibujoFantasma2Abajo									
						j continuarDibujarCasillas

		dibujarFantasma3:					# Guardar direccion en memoria de plantilla de dibujo del fantasma 3
				lw $t0, direccionF3
				beq $t0, 0, dibujarFantasma3Arriba
				beq $t0, 1, dibujarFantasma3Derecha
				beq $t0, 2, dibujarFantasma3Abajo
				beq $t0, 3, dibujarFantasma3Izquierda
				
				dibujarFantasma3Derecha:
						la $a1, dibujoFantasma3Derecha									
						j continuarDibujarCasillas																																											
				dibujarFantasma3Izquierda:
						la $a1, dibujoFantasma3Izquierda									
						j continuarDibujarCasillas
				dibujarFantasma3Arriba:
						la $a1, dibujoFantasma3Arriba									
						j continuarDibujarCasillas
				dibujarFantasma3Abajo:
						la $a1, dibujoFantasma3Abajo									
						j continuarDibujarCasillas

continuarDibujarCasillas:																		
		div $t1, $a0, 32					# Determinar fila de la casilla a dibujar
		mul $t4, $t1, 16					# Multiplicar por ancho de fila en pixeles
		mul $t4, $t4, 512					# Multiplicar por ancho de tablero en pixeles
		rem $t1, $a0, 32					# Determinar columna de la casilla
		mul $t1, $t1, 16					# Multiplicar por ancho de columna en pixeles
		add $t4, $t4, $t1					# Agregar desplazamiento de columna
		mul $t4, $t4, 4
		la $t1, pantalla					# Cargar direccion base de la pantalla
		add $t4, $t4, $t1					# Determinar direccion base en memoria de la casilla en la pantalla						
		move $t3, $zero						# Inicializar en 1er pixel de la casilla						

		dibujarPixel:				
			beq $t3, 256, finDibujarCasilla			# Terminar de dibujar la casilla si se dibujaron los 16x16 pixeles que la conforman
						
			mul $t1, $t3, 4	
			add $t6, $a1, $t1				# Calcular direccion del color del pixel en la plantilla
			lw $t6, ($t6)					# Cargar el color del pixel
																												
			div $t1, $t3, 16				
			mul $t5, $t1, 512				
			rem $t1, $t3, 16				
			add $t5, $t5, $t1	
			mul $t5, $t5, 4			
							
			add $t5, $t5, $t4				# Calcular la direccion del pixel en la pantalla
			sw $t6, ($t5)					# Guardar color del pixel en direccion correspondiente en pantalla
							
			addi $t3, $t3, 1
			j dibujarPixel
		
finDibujarCasilla:
		lw $t0, 0($sp)
		lw $t1, 4($sp)
		lw $t2, 8($sp)
		lw $t3, 12($sp)
		lw $t4, 16($sp)
		lw $t5, 20($sp)
		lw $t6, 24($sp)
		lw $t7, 28($sp)
		add $sp, $sp, 36
		jr $ra
