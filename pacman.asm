####################################### MANEJADOR DE INTERRUPCIONES #########################################
#############################################################################################################

#	Manejador de interrupciones para el juego de Pac-Man. Recibe interrupciones del teclado
# 	para cambiar la direccion de Pac-Man

.kdata
s0:									.word 0
s1:									.word 0

.ktext 0x80000180
			move $k0, $a0					# Guardamos contenido de $a0
			sw $k0, s0					
			move $k1, $v0					# Guardamos contenido de $v0
			sw $k1, s1					
					
			mfc0 $k0, $13					# Cargamos contenido del registro de causa
			andi $a0, $k0, 0x7C				# Aplicamos mascara de bits para extraer codigo de excepcion	
			bnez $a0, finManejador				# Terminar si excepcion no es del tipo interrupcion (codigo 0)
		
			mfc0 $k0, $13					# Cargamos contenido del registro de causa
			andi $a0, $k0, 0x100				# Aplicamos mascara de bits para extraer bit de interrupcion pendiente de teclado (bit 8)
			beqz $a0, finManejador				# Terminar si no es interrupcion de teclado
		
			mfc0 $k0, $13					# Cargamos contenido del registro de causa
			xor $a0, $a0, $k0				# Apagar bit de interrupcion pendiente del teclado
			mtc0 $a0, $13
		
			li $a0, 0xFFFF0004				# Direccion en memoria del registro de datos del teclado
			lw $a0, ($a0)					# Cargar caracter enviado por el teclado
			
			beq $a0, 'w', arribaMover			# Verifica si el caracter ingresado fue 'w'
			beq $a0, 's', abajoMover			# Verifica si el caracter ingresado fue 's'
			beq $a0, 'a', izquierdaMover			# Verifica si el caracter ingresado fue 'a'
			beq $a0, 'd', derechaMover			# Verifica si el caracter ingresado fue 'd'
			beq $a0, ' ', salir				# Termina el juego al presionar espacio
			j finManejador
	
			arribaMover:
					li $a0, 0 			# Cambia la direccion de Pac-Man hacia arriba
					j guardarDireccion
			derechaMover:			
					li $a0, 1 			# Cambia la direccion de Pac-Man hacia la derecha
					j guardarDireccion
			abajoMover:
					li $a0, 2 			# Cambia la direccion de Pac-Man hacia abajo
					j guardarDireccion
			izquierdaMover:
					li $a0, 3			# Cambia la direccion de Pac-Man hacia la izquierda
					j guardarDireccion

						
			guardarDireccion: 	
					sw $a0, direccionPacMan
		
finManejador:
			lw $a0, s0					# Recuperar contenido de registro antes de la llamada al manejador
			lw $v0, s1					# Recuperar contenido de registro antes de la llamada al manejador
			mtc0 $zero, $13					# Vaciar registro de causa
			mfc0 $k0, $12					# Cargar contenido del registro de estado
			ori  $k0, 0x1					# Rehabilitar interrupciones
			mtc0 $k0, $12
			eret						# Retornar de la excepcion
