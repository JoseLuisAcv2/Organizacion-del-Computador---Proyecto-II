				# Cerrar archivo
				li $v0, 16			
				move $a0, $t0					# Descriptor de archivo a cerrar	
				syscall
				###############################################
				
				
				############ Activar Interrupciones s###########
				mfc0 $t0, $12					# Cargar contenido del registro de estado
				ori $t0, $t0, 0x00000101			# Habilitar interrupciones (bit 0) y activar la interrupcion del teclado (bit 8)
				mtc0 $t0, $12
			
			   	li $t0, 0xFFFF0000     				# Direccion de registro de control del teclado
				lw $t1, ($t0)					# Cargar contenido del registro de control del teclado
				ori $t1, $t1, 0x00000002     			# Habilitar interrupciones del teclado					
				sw $t1, ($t0)
				###############################################				
				
				
				############### Crear el tablero ##############
				la $a0, buffer					# Pasar direccion en memoria como argumento 1
				la $a1, tablero
				jal inicializarTablero				# Inicializa el tablero con el input leido
				
				# Verificar que el tablero es valido
				li $v0, 4
				li $t1, 0 					# Total de errores
				
				# Verifica si hay caracteres invalidos en el tablero
				lw $t0, cantidadErrores
				beqz $t0, cont1					
				la $a0, mensajeCaracteresInvalidos		
				syscall
				add $t1, $t1, 1
				
				# Verifica si hay al menos un Pac-Man en el tablero
cont1:				lw $t0, cantidadPacMan
				bnez  $t0, cont2				 			
				la $a0, mensajeNoPM						
				syscall
				add $t1, $t1, 1				
				
				# Verifica si hay mas de un Pac-Man en el tablero
cont2:				ble $t0, 1, cont3				
				la $a0, mensajeMuchosPM
				syscall
				add $t1, $t1, 1
				
				# Verifica si la cantidad de fantasmas es 3
cont3:				lw $t0, cantidadFantasmas
				add $t0, $t0, -3
				beqz $t0, cont4
				la $a0, mensajeCantidadFantasmas
				syscall
				add $t1, $t1, 1
				
				# Verifica si hubo algun error
cont4:				beqz $t1, crearTablero
				la $a0, ERROR					# Mostrar mensaje que indica que no se pudo iniciar el juego
				syscall
				b salir
				
				
				############## Juego #################
				# Muestra el tablero si el input es valido
crearTablero:			li $v0, 4
				la $a0, mensajeInicio				# Se indica que se esta iniciando el juego
				syscall
				jal dibujarTablero				# Dibuja el tablero en la pantalla
				la $a0, tablero					# Se pasa la direccion del tablero como argumento
				
				
				jal juego					# Se inicia el juego
				move $s0, $v0					# Mover el puntaje total en $s0
				
				# Mostrar mensaje de finalizacion del juego
				li $v0, 4
				la $a0, mensajeFin				
				syscall
				# Mostrar puntacion total
				move $a0, $s0
				li $v0, 1
				syscall
				
				lw $s1, puntosTotales
				beq $s0, $s1, ganarJuego
				b salir

ganarJuego:			li $v0, 4
				la $a0, mensajeGanarJuego
				syscall								
				 
				############## Finalizar programa ##############
salir:				
				li $t1, 0					# Contador
				cicloSalirDelJuego:
					beq $t1, 512, todaviaSaliendoDelJuego
					mul $t3, $t1, 4				# Desplazamiento
					lw $t2, mascaraGameOver($t3)		# Pixel en mensaje
					beqz $t2, continuarCicloSalirDelJuego
					move $a0, $t1
					li $a1, 'M'
					jal dibujarCasilla
					continuarCicloSalirDelJuego:
						addi $t1, $t1, 1
						j cicloSalirDelJuego
								

todaviaSaliendoDelJuego:	move $a1, $s0
				la $s4, digitos
				add $s4, $s4, 4				# Apuntador a la direccion de memoria donde se almacenara el digito
				li $s5, 5				# Contador de digitos

cicloDigitos:			beqz $s5, finCicloDigitos
				rem $t1, $a1, 10			# Calcula el digito correspondiente
				div $a1, $a1, 10
				add $t1, $t1, '0'			# Convierte a caracter
				sb  $t1, ($s4)				# Almacena el digito
				add $s4, $s4, -1			# Disminuye el apuntador
				add $s5, $s5, -1			# Disminuye el contador
				b cicloDigitos

finCicloDigitos:		li $s5, 0
				li $s6, 642
				cicloImprimir:
				beq $s5, 5, salirDelJuego
				lb $a0, digitos($s5)
				move $a1, $s6
				jal imprimirDigito
				addi $s5, $s5, 1
				addi $s6, $s6, 6
				j cicloImprimir
						
salirDelJuego:			li $v0, 10
				syscall
