################################################# juego #####################################################
# VARIABLES LOCALES
# $s0 - Puntaje del juego
# $s1 - Vidas de Pac-Man
# RETORNA
# $v0 - puntaje del juego
#
juego:				
				#Almacena $ra y $s0 en la pila, responsabilidad del llamado
				add $sp, $sp, -8				# Se actualiza el valor de stack point
				sw  $ra, 4($sp)					# Se almacena $ra en la pila
				sw  $s0, 8($sp)					# Se almacena $s0 en la pila
					
				jal inicializarDirecciones			# Inicializa las direcciones de los personajes
				li $s0, 0					# Inicializa el score del juego
				li $s1, 3					# Inicializa la cantidad de vidas de Pacman
				
				
cicloJuego:			# Verificar si el juego ha sido ganado
				lw $t4, puntosTotales
				beq $t4, $s0, finJuego
				
				# Retardar Juego
				li $t7, 16384
				ciclo:	nop
					add $t7, $t7, -1
					beqz $t7, fin
					b ciclo
				fin:
				# Mover Pac-Man
				lw $a0, posicionPacMan				# Argumento 1, posicion del Pac-Man
				sw $a0, prevPosPacMan				# Almacena la posicion anterior del Pac-Man
				lw $a1, direccionPacMan				# Argumento 2, direccion del Pac-Man
				jal moverPacMan					# Llamada a la funcion
				sw $v0, posicionPacMan				# Almacena la nueva poscion del Pac Man
				
				# Mover el fantasma 1
				lw $a0, posicionF1				# Argumento 1, posicion inicial del fantasma
				sw $a0, prevPosF1				# Almacena la posicion anterior del fantasma
				lw $a1, direccionF1				# Argumento 2, direccion final del fantasma
				jal moverFantasma				# Llamada a la funcion
				sw $v0, posicionF1				# Almacena la nueva posicion
				sw $v1, direccionF1				# Almacea la nueva direccion
	
				# Mover el fantasma 2
				lw $a0, posicionF2				# Argumento 1, posicion inicial del fantasma
				sw $a0, prevPosF2				# Almacena la posicion anterior del fantasma
				lw $a1, direccionF2				# Argumento 2, direccion final del fantasma
				jal moverFantasma				# Llamada a la funcion
				sw $v0, posicionF2				# Almacena la nueva posicion
				sw $v1, direccionF2				# Almacea la nueva direccion
		
				# Mover el fantasma 3
				lw $a0, posicionF3				# Argumento 1, posicion inicial del fantasma
				sw $a0, prevPosF3				# Almacena la posicion anterior del fantasma
				lw $a1, direccionF3				# Argumento 2, direccion final del fantasma
				jal moverFantasma				# Llamada a la funcion
				sw $v0, posicionF3				# Almacena la nueva posicion
				sw $v1, direccionF3				# Almacea la nueva direccion
	
		
				# Verifica si algun fantasma alcanzo a Pac-Man	
				lw  $t0, posicionPacMan
				lw  $t2, prevPosPacMan
				
				# Verifica si lo alcanzo el fantasma 1
				lw  $t1, posicionF1
				lw  $t3, prevPosF1
				beq $t0, $t1, perderVida				
				sub $t1, $t1, $t2
				sub $t3, $t0, $t3
				mul $t1, $t1, $t3
				beqz $t1, perderVida
			
				# Verifica si lo alcanzo el fantasma 2
				lw  $t1, posicionF2
				lw  $t3, prevPosF2
				beq $t0, $t1, perderVida			
				sub $t1, $t1, $t2
				sub $t3, $t0, $t3
				mul $t1, $t1, $t3
				beqz $t1, perderVida
				
				# Verifica si lo alcanzo el fantasma 3
				lw  $t1, posicionF3
				lw  $t3, prevPosF2
				beq $t0, $t1, perderVida			
				sub $t1, $t1, $t2
				sub $t3, $t0, $t3
				mul $t1, $t1, $t3
				beqz $t1, perderVida
				
				lb  $a0, tablero($t0)				# Caracter que se encuentra en la nueva posicion del Pac-Man
				beq $a0, 'C', cereza				# Verifica si el Pac-Man se comio una cereza
				beq $a0, 'a', alimento				# Verifica si el Pac-Man se comio un alimento
				j seguir

cereza:				li $v0, 31					# Sonido al comer una cereza
				li $a0, 75
				li $a1, 700
				li $a2, 123
				li $a3, 120
				syscall	

				add $s0, $s0, 10				# Aumenta 10 puntos el score
				sb  $zero, tablero($t0)				# Elimina la cereza del tablero
				j seguir

alimento:			li $v0, 31					# Sonido al ganar puntos
				li $a0, 80
				li $a1, 500
				li $a2, 0
				li $a3, 120
				syscall	

				add $s0, $s0, 1					# Aumenta 1 punto el score
				sb  $zero, tablero($t0)				# Elimina el alimento del tablero
				j seguir

perderVida:			li $v0, 31					# Sonido de perdida de vida
				li $a0, 40
				li $a1, 350
				li $a2, 120
				li $a3, 120
				syscall				
				
				add $s1, $s1, -1
				beqz $s1, finJuego
				lw $t0, inicioPacMan
				sw $t0, posicionPacMan

seguir:				#################### ACTUALIZAR INTERFAZ ################
				lw $a0, prevPosPacMan
				lb $a1, tablero($a0)
				jal dibujarCasilla				# Actualiza casilla de donde venia Pac-Man
				
				lw $a0, prevPosF1
				lb $a1, tablero($a0)
				jal dibujarCasilla				# Actualiza casilla de donde venia el fantasma 1
				
				lw $a0, prevPosF2
				lb $a1, tablero($a0)
				jal dibujarCasilla				# Actualiza casilla de donde venia el fantasma 2
				
				lw $a0, prevPosF3
				lb $a1, tablero($a0)
				jal dibujarCasilla				# Actualiza casilla de donde venia el fantasma 3
				
				lw $a0, posicionPacMan
				li $a1, '<'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue Pac-Man
				
				lw $a0, posicionF1
				li $a1, '1'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue el fantasma 1
				
				lw $a0, posicionF2
				li $a1, '2'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue el fantasma 2
				
				lw $a0, posicionF3
				li $a1, '3'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue el fantasma 3																			
																		
				j cicloJuego


finJuego:							
				move $v0, $s0
				# Se recupera $ra y $s0 de la pila
				lw  $ra, 4($sp)					# Recupera $ra de la pila
				lw  $s0, 8($sp)					# Recupera $s0 de la pila
				add $sp, $sp, 8					# Se actualiza el valor de stack point
				jr $ra
