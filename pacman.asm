				jal dibujarCasilla
				addi $t0, $t0, 1
				j dibujarCasillas
					
finDibujarTablero:		lw $a0, posicionPacMan
				li $a1, '<'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue Pac-Man
				
				lw $a0, posicionF1
				li $a1, '1'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue el fantasma 1
				
				lw $a0, posicionF2
				li $a1, '2'
				jal dibujarCasilla				# Actualiza casilla hacia donde fue el fantasma 2
				
				lw $a0, posicionF3
				li $a1, '3'
				jal dibujarCasilla
				
				lw $ra, ($sp)
				add $sp, $sp, 4
				jr $ra					

###################################################################### dibujarCasilla ############################################################################
# Dibujar Casilla: Dibujar casilla en pantalla
