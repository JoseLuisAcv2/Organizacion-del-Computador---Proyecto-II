################################################## moverPacMan #####################################################
# PARAMETROS
# $a0 - posicion inicial del Pac-Man
# $a1 - direccion inicial del Pac-Man
# RETORNA
# $v0 - posicion final del Pac-Man
# VARIABLESLOCALES
# $t0 - fila correspondiente a la posicion del Pac-Man
# $t1 - columna correspondiente a la posicion del Pac-Man

moverPacMan:
				lw $t0, bocaPacMan				# Estado de apertura de Pac-Man
				nor $t0, $t0, $t0
				sw $t0, bocaPacMan 
				
				div  $t0, $a0, 32				# Calcula la fila donde se encuentra el Pac-Man
				rem  $t1, $a0, 32				# Calcula la columna donde se encuentra el Pac-Man
				move $v0, $a0					# Almacena la posicion inicial del Pac-Man en $vo
				
				# Determina la direccion hacia la que se mueve el Pac-Man
				beq $a1, 0, arribaPM				
				beq $a1, 1, derechaPM				
				beq $a1, 2, abajoPM				
				beq $a1, 3, izquierdaPM
				
				# Mueve el Pac-Man segun la direccion dada			
arribaPM:			add $t0, $t0, -1				# Se mueva hacia arriba	
				j nuevaPosicionPM					
derechaPM:			add $t1, $t1, 1					# Se mueve a la derecha
				j nuevaPosicionPM
abajoPM:			add $t0, $t0, 1					# Se mueve hacia abajo
				j nuevaPosicionPM
izquierdaPM:			add $t1, $t1, -1				# Se mueve a la izquierda
				j nuevaPosicionPM

				# Verifica la posicion de Destino
nuevaPosicionPM:		bltz $t0, finMoverPacMan			# Verifica que la fila destino sea mayor que 0		
				bltz $t1, finMoverPacMan			# Verifa que la columna destino sea mayor que 0
				bge  $t0, 32, finMoverPacMan			# Verifica que la filda destino sea menor que 32
				bge  $t1, 32, finMoverPacMan			# Verifica que la columna destino se menor que 32
				mul  $t5, $t0, 32
				add  $t5, $t5, $t1				# Calcula la posicion relativa al tablero
				lb   $t2, tablero($t5)				# Carga el elemento en el tablero de la posicion de destino
				beq  $t2, 'P', finMoverPacMan			# Verifica que la posicion de destino no sea una Pared					
				move $v0, $t5					# Se mueve el fantasma si la posicion de destino es valida

finMoverPacMan:			jr $ra						# Fin de la funcion
