################################################ moverFantasma #####################################################
# PARAMETROS
# $a0 - posicion inicial 
# $a1 - direccion inicial
# RETORNA
# $v0 - posicion final
# $v1 - direccion final
# VARIABLES LOCALES:
# $t0 - fila correspondiente a la posicion del fantasma
# $t1 - columna correspondiente a la posicion del fantasma
# $t2 - elemento del tablero en la posicion destino
# $t3 - fila de la posicion inicial del fantasma
# $t4 - columna de la posicion inicial del fantasma
# $t5 - posicion de destino del fantasma


moverFantasma:			div  $t3, $a0, 32				# Calcula la fila donde se encuentra el fantasma
				rem  $t4, $a0, 32				# Calcula la columna donde se encuentra el fantasma
				move $v1, $a1					# Inicializa la direccion del fantasma
										
				
				# Determina la direccion hacia la que se mueve el fantasma
intentarMoverse:		beq $v1, 0, arriba				
				beq $v1, 1, derecha				
				beq $v1, 2, abajo				
				beq $v1, 3, izquierda
				# Mueve el fantasma segun la direccion dada			
arriba:				add $t0, $t3, -1				# Se mueva hacia arriba	
				move $t1, $t4					# La columna permanece igual
				j nuevaPosicion					
derecha:			add $t1, $t4, 1					# Se mueve a la derecha
				move $t0, $t3					# La fila permanece igual
				j nuevaPosicion
abajo:				add $t0, $t3, 1					# Se mueve hacia abajo
				move $t1, $t4					# La columna permanece igual
				j nuevaPosicion
izquierda:			add $t1, $t4, -1				# Se mueve a la izquierda
				move $t0, $t3					# La fila permanece igual
				j nuevaPosicion
				
				# Verifica la posicion de Destino
nuevaPosicion:			bltz $t0, cambiarDireccion			# Verifica que la fila destino sea mayor que 0		
				bltz $t1, cambiarDireccion			# Verifa que la columna destino sea mayor que 0
				bge  $t0, 32, cambiarDireccion			# Verifica que la filda destino sea menor que 32
				bge  $t1, 32, cambiarDireccion			# Verifica que la columna destino se menor que 32
				mul  $t5, $t0, 32
				add  $t5, $t5, $t1				# Calcula la posicion relativa al tablero
				lb   $t2, tablero($t5)				# Carga el elemento en el tablero de la posicion de destino
				beq  $t2, 'P', cambiarDireccion			# Verifica que la posicion de destino no sea una Pared					
				j finMoverFantasma				# Se mueve el fantasma si la posicion de destino es valida
								
cambiarDireccion:		li $v0, 42					# Generar numeros psudo aleatorios:
				li $a1, 4					# Configura la cantidad de los numeros generados
				syscall 					# Llamada al sistema
				move $v1, $a0					# Cambia la direccion
				j intentarMoverse 				# Intenta moverse nuevamente

finMoverFantasma:		move $v0, $t5					# Guarda la posicion final
				jr $ra						# Fin de la funcion

