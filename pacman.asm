		sw $t3, 12($sp)
		sw $t4, 16($sp)
		sw $t5, 20($sp)
		sw $t6, 24($sp)
		sw $t7, 28($sp)

		beq $a1, 'P', dibujarPared
		beq $a1, 'a', dibujarComida
		beq $a1, 'C', dibujarCereza
		beq $a1,  0 , dibujarVacio
		beq $a1, '<', dibujarPacMan
		beq $a1, '1', dibujarFantasma1
		beq $a1, '2', dibujarFantasma2
		beq $a1, '3', dibujarFantasma3
		beq $a1, 'M', dibujarMensajeFinJuego
			
		dibujarMensajeFinJuego:					# Guardar direccion en memoria de plantilla de dibujo de un pixel del mensaje final
				la $a1, dibujoGameOver
				j continuarDibujarCasillas							
		dibujarPared:						# Guardar direccion en memoria de plantilla de dibujo de una pared
				la $a1, dibujoPared									
				j continuarDibujarCasillas
		dibujarComida:						# Guardar direccion en memoria de plantilla de dibujo de la comida
				la $a1, dibujoComida
