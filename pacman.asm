############################################# Imprimir digito ##############################################
# Imprimir digito: Imprime digito de la puntuacion final. El tamano del digito son 4x5 casillas del tablero
# PARAMETROS:
# $a0 - Digito a imprimir
# $a1 - Posicion en pantalla de esquina superior izquierda
# VARIABLES LOCALES:
# $t0 - Plantilla del digito
# $t1 - Posicion en plantilla del digito (casilla en pantalla)
# $t2 - Valor de la casilla
# $t3 - Posicion real en pantalla
imprimirDigito:
		addi, $sp, $sp, -4
		sw $ra, ($sp)
		
		beq $a0, '0', imprimir0
		beq $a0, '1', imprimir1		
		beq $a0, '2', imprimir2
		beq $a0, '3', imprimir3
		beq $a0, '4', imprimir4
		beq $a0, '5', imprimir5
		beq $a0, '6', imprimir6
		beq $a0, '7', imprimir7
		beq $a0, '8', imprimir8
		beq $a0, '9', imprimir9
					
		imprimir0:
			la $t0, digito0
			j imprimirDigitoEnPantalla
		imprimir1:
			la $t0, digito1
			j imprimirDigitoEnPantalla
		imprimir2:
			la $t0, digito2
			j imprimirDigitoEnPantalla
		imprimir3:
			la $t0, digito3
			j imprimirDigitoEnPantalla
		imprimir4:
			la $t0, digito4
			j imprimirDigitoEnPantalla
		imprimir5:
			la $t0, digito5
			j imprimirDigitoEnPantalla
		imprimir6:
			la $t0, digito6
			j imprimirDigitoEnPantalla
		imprimir7:
			la $t0, digito7
			j imprimirDigitoEnPantalla
		imprimir8:
			la $t0, digito8
			j imprimirDigitoEnPantalla
		imprimir9:
			la $t0, digito9
			j imprimirDigitoEnPantalla

imprimirDigitoEnPantalla:
		li $t1, 0

cicloImprimirDigitos:		
		beq $t1, 20, finImprimirDigitos
		lw $t2, ($t0)
		
		beq $t2, 0, aumentarContadores
		
		
		rem $t5, $t1, 4			# Columna relativa a la letra
		div $t6, $t1, 4			# Fila relativa a la letra
		mul $t6, $t6, 32
		
		add $t4, $t5, $a1
		add $t4, $t4, $t6
		
		sub $sp, $sp, 36
		sw $a0, 0($sp)
		sw $t0, 4($sp)
		sw $t1, 8($sp)
		sw $t2, 12($sp)
		sw $t3, 16($sp)
		sw $t4, 20($sp)
		sw $t5, 24($sp)
		sw $t6, 28($sp)
		sw $a1, 32($sp)
		
		move $a0, $t4
		li $a1, 'M'
		jal dibujarCasilla
	
		lw $a0, 0($sp)
		lw $t0, 4($sp)
		lw $t1, 8($sp)
		lw $t2, 12($sp)
		lw $t3, 16($sp)
		lw $t4, 20($sp)
		lw $t5, 24($sp)
		lw $t6, 28($sp)
		lw $a1, 32($sp)
		addi $sp, $sp, 36
				
aumentarContadores:
		add $t0, $t0, 4
		add $t1, $t1, 1
		j cicloImprimirDigitos
				
finImprimirDigitos:
		lw $ra, ($sp)
		add $sp, $sp, 4
		jr $ra
