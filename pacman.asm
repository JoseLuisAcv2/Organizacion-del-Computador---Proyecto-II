############################################# inicializarTablero #################################################
# Inicializar Tablero: inicializa el tablero con el input leido desde el archivo
# PARAMETROS
# $a0 - Direccion de memoria del string leido
# $a1 - Direccion de memoria del arreglo del tablero
# VARIABLES LOCALES:
# $a2 - Caracter leido
# $t0 - Contador general
# $t1 - Contador de cantidad de Pac-Man
# $t2 - Contador de cantidad de fantasmas
# $t3 - Contador de cantidad de errores
# $t4 - Apuntador al caracter que sera leido
# $t5 - Apuntador al tablero
# $t6 - Contador de la columna actual
# $t7 - Acumulador de la cantidad maxima de puntos que pueden ser acumulados durante el juego
inicializarTablero:
				sw $zero, bocaPacMan				# Se inicia Pac-Man con la boca abierta
				li $t0, 0					# Inicializa el contador general
				li $t1, 0					# Inicializa el contador de cantidad de Pac-Man
				li $t2, 0					# Inicializa el contador de enemigos
				li $t3, 0					# Inicializa el contador de errores
				li $t6, 0					# Inicializa el columna actual
				li $t7, 0					# Incializa  el acumulador del puntaje en 0
				move $t4, $a0					# Inicializa la direccion del caracter que sera leido
				move $t5, $a1					# Inicializa la direccion donde se almacena dato leido

cicloTablero:			beq $t0, 1024, finCicloTablero			# Iterar sobre el tablero
				lb $a2, ($t4)					# Obtener caracter
				
				beq $a2, 'P', continuar
				beq $a2, 'C', Cereza				# Verifica si el caracter representa una Cereza
				beq $a2, 'a', Alimento				# Verifica si el caracter representa un alimento
				beq $a2, '<', PacMan				# Verifica si el caracter reoresenta a Pac-Man
				beq $a2, 'F', Fantasma				# Verifica si el caracter representa un fantasma
				j error
				
Cereza:				add  $t7, $t7, 10				# Aumenta el puntaje en 10
				j continuar
Alimento:			add  $t7, $t7, 1				# Aumenta el puntaje en 1
				j continuar
PacMan:				addi $t1, $t1, 1				# Aumenta el contador de Pac-Man
				sw $t0, posicionPacMan				# Guarda la posicion de Pac-Man
				sw $t0, inicioPacMan				# Guarda la posicion de Pac-Man
				j continuar
							
Fantasma:			add $t2, $t2, 1					# Aumenta el contador de fantasmas			
				beq $t2, 1, fantasma1				# Verifica si es el primer fantasma
				beq $t2, 2, fantasma2				# Verifica si es el segundo fantasma
				beq $t2, 3, fantasma3				# Verifica si es el tercer fantasma
				j continuar
fantasma1:			sw $t0, posicionF1				# Guarda la posicion del fantasma 1
				j continuar
fantasma2:			sw $t0, posicionF2				# Guarda la posicion del fantasma 2
				j continuar
fantasma3:			sw $t0, posicionF3				# Guarda la posicion del fantasma 3
				j continuar				
error:				addi $t3, $t3, 1				# Si no es un caracter valido, aumenta el contador de errores
				
continuar:			sb $a2, ($t5)					# Almacena el caracter
				add $t0, $t0, 1					# Aumenta el contador general
				addi $t4, $t4, 1				# Aumenta el apuntador del caracter
				addi $t5, $t5, 1				# Aumenta el apuntador del tablero
				addi $t6, $t6, 1				# Aumenta el numero de columna
				
				blt $t6, 32, cicloTablero			# Verifica si finalizo de leer la fila
				addi $t4, $t4, 1				# Salta el salto de linea
				li $t6, 0					# Restablece el numero de columna a 0
				
				j cicloTablero

finCicloTablero:		lw $t5, posicionPacMan
				sb $zero, tablero($t5)				# Elimina a Pac-Man del tablero
				lw $t5, posicionF1
				sb $zero, tablero($t5)				# Elimina al fantasma 1 del tablero
				lw $t5, posicionF2
				sb $zero, tablero($t5)				# Elimina al fantasma 2 del tablero
				lw $t5, posicionF3
				sb $zero, tablero($t5)				# Elimina al fantasma 3 del tablero
				
				sw $t1, cantidadPacMan				# Almacena la cantidad de Pac-Man encontrados
				sw $t2, cantidadFantasmas			# Almacena la cantidad de fantasmas encontrados
				sw $t3, cantidadErrores				# Almacena la cantidad de caracteres no validos encontrados
				sw $t7, puntosTotales				# Almacena la cantidad de puntos totales
				jr $ra						# Retorna la funcion
